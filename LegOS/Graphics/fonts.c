void draw_char(int x, int y, char* format, char c, unsigned short color) {
	int width = 4;
	int height = 6;
	char* bitmap;
	int offset = 3;
	char data = bitmap[0];

	if (strcmp(format, "4x6") == 0) {
		width = 4;
		height = 6;
		bitmap = ascii_4x6[c];
		offset = 3;
	}
	else if (strcmp(format, "5x12") == 0) {
		width = 6;
		height = 12;
		bitmap = ascii_5x12[c];
		offset = 8;
	}
	else if (strcmp(format, "6x8") == 0)
	{
		width = 6;
		height = 8;
		bitmap = ascii_6x8[c];
		offset = 6;
	}
	else if (strcmp(format, "7x12") == 0)
	{
		width = 8;
		height = 12;
		bitmap = ascii_7x12[c];
		offset = 12;
	}
	else if (strcmp(format, "8x12") == 0)
	{
		width = 8;
		height = 12;
		bitmap = ascii_8x12[c];
		offset = 12;
	}
	else if (strcmp(format, "10x18") == 0)
	{
		width = 12;
		height = 18;
		bitmap = ascii_10x18[c];
		offset = 27;
	}
	else if (strcmp(format, "12x16") == 0)
	{
		width = 12;
		height = 16;
		bitmap = ascii_12x16[c];
		offset = 24;
	}

	int h = 0;
	int w = 0;
	for (int i = 0; i < offset; i++)
	{
		data = bitmap[i];
		for (int j = 0; j < 8; j++)
		{
			if ( (data >> 7 - j) & 1 )
			{
				draw_pixel(x + w, y + h, color);
			}
			w++;
			if (w == width)
			{
				w = 0;
				h++;
			}
		}
	}		
}

void draw_string(int x, int y, char* format, char* str1, unsigned short color) {
	int height;
	int width;
	if (strcmp(format, "4x6") == 0) {
		height = 6;
		width = 4;
	}
	else if (strcmp(format, "5x12") == 0){
		width = 5;		
		height = 12;
	}
	else if (strcmp(format, "6x8") == 0){
		width = 6;
		height = 8;
	}
	else if (strcmp(format, "7x12") == 0){
		width = 7;
		height = 12;
	}
	else if (strcmp(format, "8x12") == 0){
		width = 8;
		height = 12;
	}
	else if (strcmp(format, "10x18") == 0){
		width = 10;
		height = 18;
	}
	else if (strcmp(format, "12x16") == 0){
		width = 12;
		height = 16;
	}
	while(*str1) {
		if (*str1 == '\n' || x >= 320) {
			x = 0;
			y += height;
			str1++;
		}
		if (*str1 == '\t') {
			for (int i = 0; i < 4; i++)
			{
				x += width;
			}
			str1++;
		}
		if (*str1 == '\r') {
			x = 0;
			str1++;
		}
		if (x > 320)
		{
			x = 0;
			y += height;
		}
		
		if (x < 320 && y < 200) {
			draw_char(x, y, format, *str1, color);
		}
		str1++;
		x += width;
	}
};

